#vamos a aprender todos los tipos de operadores matematicos

y = 23
x = 32

#suma
print(f'{x} mas {y} es igual a {x+y}')

#resta
print(f'{x} menos {y} es igual a {x-y}')

#multiplicacion
print(f'{x} por {y} es igual a {x*y}')

#division
print(f'{x} dividido {y} es igual a {x/y}')

#division al piso (deja solo el primer numero)
print(f'{x} dividido al piso de {y} es igual a {x//y}')

#el modulo (el resto de una division)
print(f'el modulo de  {x} divido {y} es igual a {x%y}')

#potencia al cubo o al cuadrado por ej
print(f'{x} elevado a {2}(cuadrado) es igual a {x**2}')
print(f'{x} elevado a {3}(cubo) es igual a {x**2}')

#raiz cuadrada
print(f'la raiz cuadrada de {x} es {x**0.5}')