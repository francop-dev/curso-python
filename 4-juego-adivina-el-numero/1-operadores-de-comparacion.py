'''la diferencia entre usar = y doble ==
es que el = es utilizado para variables pero == da una comparacion, es decir 5 == 5 y dara un True como resultado'''

mi_bool = 5 == 5
print(mi_bool)#da True

#tambien hay comparaciones de mayor, menor, menor/igual, mayor/igual y diferente

mi_bool = 54 <= 1 # 54 es menor o igual a 1 ?
print(mi_bool) # va a dar False

mi_bool = 12 >= 23 # 12 es mayor o igual a 23?
print(mi_bool) # va a dar falso

mi_bool = 12 != 12 # 12 es distinto a 12?
